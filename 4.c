#include <stdio.h>
#include <math.h> // Необхідно для функції log10()

int main() {
    // Вхідні дані згідно з варіантом
    double a = 0.0;
    double b = 3.0;
    double dx = 0.025;
    double x, y;

    // Виводимо заголовок таблиці
    printf("---------------------------\n");
    printf("|    x      |      y      |\n");
    printf("---------------------------\n");

    // Цикл для обчислення значень від a до b з кроком dx
    // Використовуємо невелику добавку (dx/2), щоб уникнути проблем із точністю double
    for (x = a; x <= b + dx/2; x += dx) {
        
        // ПЕРЕВІРКА ВИНЯТКОВИХ СИТУАЦІЙ:
        // 1. Логарифм lg(x) визначений тільки для x > 0
        // 2. Знаменник (4x + 13) не має дорівнювати 0 (тут він завжди > 0 при x >= 0)
        
        if (x <= 0) {
            // Виводимо повідомлення про помилку для x = 0
            printf("| %9.3f | undefined |\n", x);
        } 
        else {
            // Обчислюємо функцію: log10(x) - це десятковий логарифм
            y = log10(x) / (4 * x + 13);
            
            // Виводимо значення x та y у табличному форматі
            // %9.3f - ширина поля 9 символів, 3 знаки після коми
            printf("| %9.3f | %11.6f |\n", x, y);
        }
    }

    printf("---------------------------\n");

    return 0;
}