#include <stdio.h>

int main() {
    int n;           // Натуральне число n
    double x;        // Дійсне число x
    double total_sum = 0.0; // Змінна для накопичення загального результату

    // Введення даних
    printf("Введіть натуральне число n: ");
    if (scanf("%d", &n) != 1 || n < 1) { // Перевірка, чи n є натуральним
        printf("Помилка: n має бути натуральним числом (1, 2, 3...)\n");
        return 1;
    }

    printf("Введіть дійсне число x: ");
    scanf("%lf", &x); // %lf використовується для зчитування double

    // --- ОБЧИСЛЕННЯ ---
    // Зовнішній цикл: від i = 1 до n
    for (int i = 1; i <= n; i++) {
        
        double internal_sum = 0.0; // Сума для внутрішнього циклу

        // Внутрішній цикл: від j = i до n-1
        // Увага: якщо i >= n, цей цикл не виконається жодного разу
        for (int j = i; j <= n - 1; j++) {
            if (j != 0) { // Захист від ділення на нуль (хоча j починається з i=1)
                internal_sum += x / j;
            }
        }

        // Додаємо результат внутрішнього циклу до загальної суми
        total_sum += internal_sum;
    }

    // Виведення результату
    // %.4f виведе число з 4 знаками після коми
    printf("Результат обчислення подвійної суми: %.4f\n", total_sum);

    return 0;
}